//Data types in JS

//STRING
var fruit = "apple";
var myBreakfast = "Today I'm eating pancakes";

//NUMBERS
var age = 26;
var daysUntilExpiration = 7;
var withDecimals = 10.3;

//BOOLEAN TRUE-FALSE
var isEdible = true;
var isRed = false;

//ARRAYS
var colors = ["blue", "red", "yellow"];

//OBJECTS
var person = {
  firstName: "Leslie",
  lasName: "Cazarez",
  age: 26,
  eyeColor: "Brown",
};

//UNDEFINED Value can be something that has to be calculated
//Any variable can be emptied, by setting the value to undefined. The type will also be undefined.
